/*Write a program that prints God bless Kenya 25 times on the screen using while
 * NB: The Phrase God Bless Kenya should appear only once in your source code.*/
public class While {
    public static void main(String[] args){
        int i=1;
        while(i<=25){

            System.out.println("God bless Kenya");
            i++;
        }
    }
}
