using System;

namespace God_bless_Kenya_using_while
{/*Write a program that prints God bless Kenya. 25 times on the screen using while*/
    class Program
    {
        static void Main(string[] args)
        {
            int i = 1;

            while (i <= 25)
            {
                Console.WriteLine("God bless Kenya");
                i++;
            }
            
        }
    }
}
