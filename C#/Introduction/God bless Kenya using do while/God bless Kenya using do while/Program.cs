using System;

namespace God_bless_Kenya_using_do_while
{/*Write a program that prints God bless Kenya. 25 times on the screen using do...while*/
    class Program
    {
        static void Main(string[] args)
        {
            int i=1;
            do {
                Console.WriteLine("God bless Kenya");
                i++;
            }
            while (i<=25);
            
        }
    }
}
