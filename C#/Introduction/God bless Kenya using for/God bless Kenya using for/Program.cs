using System;

namespace God_bless_Kenya_using_for
{/*Write a program that prints God bless Kenya. 25 times on the screen using for*/
    class Program
    {
        static void Main(string[] args)
        {
            for (int i=1; i <= 25; i++)
            {
                Console.WriteLine("God bless Kenya");
            }
            
        }
    }
}
